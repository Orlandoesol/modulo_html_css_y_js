//math.random
console.log(Math.random());

//math.round, redondea un numero al entero mas cercano
console.log(Math.round(2.6));

//math.floor, redondea un numero hacia abajo
console.log(Math.floor(2.5));

//math.ceil, redondea un numero hacia arriba
console.log(Math.ceil(2.5));

//math.trunc, elimina la parte decimal de un numero
console.log(Math.trunc(2.5));

//math.pow, eleva un numero a la potencia de otro numero
//let potencia = 2 ** 3
console.log(Math.pow(2, 3));

//math.sqrt, calcula la raiz cuadrada de un numero
console.log(Math.sqrt(9));

//math.max, devuelve el numero mas grande de una lista de numeros
let maxNum = Math.max(1, 5, 3, 8, 48); // 5
console.log(maxNum);

let minNum = Math.min(1, 5, 3, 0); // 1
console.log(minNum);

//math.abs, devuelve el valor absoluto de un numero
let absNum = Math.abs(-74); // 5
console.log(absNum);

//math.sin, devuelve el seno de un numero
let sinNum = Math.sin(30); // 0.5
console.log(sinNum);    

//math.cos, devuelve el coseno de un numero
let cosNum = Math.cos(30); // 0.5
console.log(cosNum);

//math.tan, devuelve la tangente de un numero
let tanNum = Math.tan(30); // 0.5
console.log(tanNum);